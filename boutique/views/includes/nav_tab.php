  <div class="tab-pane fade show active" id="description">
    <p>La fabrication traditionnelle du chapeau Panama se localise essentiellement dans trois provinces de l'Equateur: La province de Manabi et celles d' Azuay et de Cañar.
    Chacune de ces deux régions a développé au cours de son histoire un savoir-faire particulier, des techniques de tissages différentes sur certains points.
    Cela se traduit au niveau de la production nationale par l' identification de deux grandes familles de chapeaux: Le Montecristi et le Cuenca(Fedora).
    Notre catalogue par souci de commodité s' articule autour de cette division.</p>
    <p>L'ensemble de nos articles proviennent de cet artisanat équatorien: ils sont tissés à la main, avec de la palme Toquilla.</p>
    </div>
  <div class="tab-pane fade" id="entretien">
    <p>La palme de toquilla contient un certain degré d'humidité, selon son épaisseur; ce qui lui confère sa flexibilité.
    Quand le chapeau est exposé à une atmosphère saturée d'humidité il l'absorbe. A contrario, en atmosphère séche la palme va perdre son humidité,
    au risque de devenir cassante.
    Ces processus fragilisent sa structure avec le temps.
    Les Montecristis ne connaissent pas ce problème car ils utilisent une palme plus fine et un tissage plus dense.
    Pour protéger votre chapeau, veuillez suivre nos conseils dans la rubrique Entretien.</p>
    </div>
  <div class="tab-pane fade" id="taille">
